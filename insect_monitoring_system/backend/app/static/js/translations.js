// Function to get translated text
function getTranslation(key, language = 'en') {
    return translations[language] && translations[language][key] ? translations[language][key] : key;
}

// Function to apply translations to the page
function applyTranslations(language = 'en') {
    // Apply translations to elements with data-i18n attributes
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key, language);
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else if (element.tagName === 'OPTION') {
            element.textContent = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update title
    const titleElement = document.querySelector('title');
    if (titleElement) {
        const titleKey = titleElement.getAttribute('data-i18n') || 
                         (titleElement.textContent.includes('Dashboard') ? 'dashboardTitle' : 
                         titleElement.textContent.includes('Profile') ? 'userProfile' : 
                         titleElement.textContent.includes('Settings') ? 'settings' : 'dashboardTitle');
        titleElement.textContent = getTranslation(titleKey, language);
    }
    
    // Store current language in localStorage
    localStorage.setItem('language', language);
    
    // Dispatch custom event for other scripts to listen to language changes
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: language } }));
}

// Export for use in other scripts
window.translations = {
    getTranslation: getTranslation,
    applyTranslations: applyTranslations
};